"use strict";

var Number1 = prompt("Digite o primeiro número");
var Number2 = prompt("Digite o segundo número");
var Addition = parseFloat(Number1) + parseFloat(Number2);
var Subtraction = parseFloat(Number1) - parseFloat(Number1);
var Multiplication = parseFloat(Number1) * parseFloat(Number2);
var Division = parseFloat(Number1) / parseFloat(Number2);
alert("Esses foram os resultados\n1- Adi\xE7\xE3o: ".concat(Addition, "\n2- Subtra\xE7\xE3o: ").concat(Subtraction, "\n3- Multiplica\xE7\xE3o: ").concat(Multiplication, "\n4- Divis\xE3o: ").concat(Division, " "));