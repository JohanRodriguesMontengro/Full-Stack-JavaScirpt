let Number1 = prompt("Digite o primeiro número");
let Number2 = prompt("Digite o segundo número");

let Addition = parseFloat(Number1) + parseFloat(Number2);
let Subtraction = parseFloat(Number1) - parseFloat(Number1);
let Multiplication = parseFloat(Number1) * parseFloat(Number2);
let Division = parseFloat(Number1) / parseFloat(Number2);

alert(`Esses foram os resultados
1- Adição: ${Addition}
2- Subtração: ${Subtraction}
3- Multiplicação: ${Multiplication}
4- Divisão: ${Division} `)